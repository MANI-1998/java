package while_concepts;

public class While_1 {
	
	public static void main(String[] args) {
		//initialisation
		int n=10;
		//condition
		while (n>7) {
		//iteration 
			n--;
			System.out.println("true");
		
		}
		
		System.out.println("--------------------------------");
		
		System.out.println("false");
	}
}
